// Strings are Immutable.They cannot be changed.

let name = "Divyesh";
console.log(name);

// To find the length of the string 
console.log(name.length);

let friend = 'Harry';
console.log(friend);

// To access any character at particular index in a string
console.log(name[3]);

// Using backward single quotes we can print variables directly
console.log(`${friend} is a friend of ${name}`);

// Escape sequence characters
let fruit = 'Bana\'na';
console.log(fruit);
console.log(fruit.length);